# –§–ò–õ–¨–¢–†–ê–¶–ò–Ø –°–ï–ì–ú–ï–ù–¢–û–í-–ì–ê–õ–õ–Æ–¶–ò–ù–ê–¶–ò–ô
# ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê

def filter_hallucination_segments(segments):
    """
    üÜï v16.3.2: –£–¥–∞–ª—è–µ—Ç —Å–µ–≥–º–µ–Ω—Ç—ã, –∫–æ—Ç–æ—Ä—ã–µ –ø–æ–ª–Ω–æ—Å—Ç—å—é —è–≤–ª—è—é—Ç—Å—è –≥–∞–ª–ª—é—Ü–∏–Ω–∞—Ü–∏—è–º–∏

    –ò—Å–ø–æ–ª—å–∑—É–µ—Ç –∫–∞–∫ —Å—Ç–∞—Ä—É—é –ª–æ–≥–∏–∫—É (–ø–∞—Ç—Ç–µ—Ä–Ω—ã), —Ç–∞–∫ –∏ –Ω–æ–≤—É—é (confidence + –ø–æ–≤—Ç–æ—Ä—ã)

    Args:
        segments: –°–ø–∏—Å–æ–∫ —Å–µ–≥–º–µ–Ω—Ç–æ–≤

    Returns:
        –û—Ç—Ñ–∏–ª—å—Ç—Ä–æ–≤–∞–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫ —Å–µ–≥–º–µ–Ω—Ç–æ–≤
    """
    filtered = []
    removed_count = 0

    for seg in segments:
        text = seg.get("text", "").strip()
        speaker = seg.get("speaker", "")

        # –°—Ç–∞—Ä–∞—è –ª–æ–≥–∏–∫–∞: –ø–∞—Ç—Ç–µ—Ä–Ω—ã
        if is_hallucination(text) and speaker not in ["–ñ—É—Ä–Ω–∞–ª–∏—Å—Ç", "–û–ø–µ—Ä–∞—Ç–æ—Ä"]:
            removed_count += 1
            print(f"  üóëÔ∏è [{seg.get('start_hms', '??:??:??')}] \"{text[:30]}\" (–ø–∞—Ç—Ç–µ—Ä–Ω)")
            continue

        # üÜï v16.3.2: –ù–æ–≤–∞—è –ª–æ–≥–∏–∫–∞ - confidence + –ø–æ–≤—Ç–æ—Ä—ã
        if is_whisper_hallucination(seg, segments):
            removed_count += 1
            confidence = seg.get('confidence', 0)
            print(f"  üóëÔ∏è [{seg.get('start_hms', '??:??:??')}] \"{text[:30]}\" (conf={confidence:.2f})")
            continue

        filtered.append(seg)

    if removed_count > 0:
        print(f"  üßπ –£–¥–∞–ª–µ–Ω–æ –≥–∞–ª–ª—é—Ü–∏–Ω–∞—Ü–∏–π: {removed_count}")

    return filtered
