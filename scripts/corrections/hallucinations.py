import re
from core.config import HALLUCINATION_PATTERNS, HALLUCINATION_CLEANUP_MODE


# ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
# –ü–†–û–í–ï–†–ö–ê –ù–ê –ì–ê–õ–õ–Æ–¶–ò–ù–ê–¶–ò–ò
# ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê

def is_hallucination(text):
    """
    –ü—Ä–æ–≤–µ—Ä—è–µ—Ç, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —Ç–µ–∫—Å—Ç –≥–∞–ª–ª—é—Ü–∏–Ω–∞—Ü–∏–µ–π Whisper

    Args:
        text: –¢–µ–∫—Å—Ç –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏

    Returns:
        bool: True –µ—Å–ª–∏ –≥–∞–ª–ª—é—Ü–∏–Ω–∞—Ü–∏—è
    """
    text_clean = text.strip()
    word_count = len(text_clean.split())

    for pattern in HALLUCINATION_PATTERNS:
        # –ü–∞—Ç—Ç–µ—Ä–Ω—ã —Å ^ —Ç—Ä–µ–±—É—é—Ç —Ç–æ—á–Ω–æ–≥–æ —Å–æ–≤–ø–∞–¥–µ–Ω–∏—è —Å –Ω–∞—á–∞–ª–æ–º
        if pattern.startswith('^'):
            if re.match(pattern, text_clean, re.I):
                return True
        else:
            if re.search(pattern, text_clean, re.I):
                # –î–ª–∏–Ω–Ω—ã–µ —Å–µ–≥–º–µ–Ω—Ç—ã –Ω–µ —Å—á–∏—Ç–∞–µ–º –≥–∞–ª–ª—é—Ü–∏–Ω–∞—Ü–∏–µ–π
                if word_count > 50:
                    continue
                return True

    return False


def is_whisper_hallucination(seg, all_segments):
    """
    üÜï v16.3.2: –û–ø—Ä–µ–¥–µ–ª—è–µ—Ç, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —Å–µ–≥–º–µ–Ω—Ç –≥–∞–ª–ª—é—Ü–∏–Ω–∞—Ü–∏–µ–π Whisper

    –ü—Ä–∏–∑–Ω–∞–∫–∏ –≥–∞–ª–ª—é—Ü–∏–Ω–∞—Ü–∏–∏:
    1. –û—á–µ–Ω—å –Ω–∏–∑–∫–∏–π confidence (< -0.5)
    2. –ö–æ—Ä–æ—Ç–∫–∞—è –¥–ª–∏—Ç–µ–ª—å–Ω–æ—Å—Ç—å (< 2s)
    3. –¢–µ–∫—Å—Ç –ø–æ–≤—Ç–æ—Ä—è–µ—Ç—Å—è –≤ –¥—Ä—É–≥–∏—Ö —Å–µ–≥–º–µ–Ω—Ç–∞—Ö
    4. –°–æ—Å—Ç–æ–∏—Ç –∏–∑ 1-2 —Å–ª–æ–≤

    Args:
        seg: –ü—Ä–æ–≤–µ—Ä—è–µ–º—ã–π —Å–µ–≥–º–µ–Ω—Ç
        all_segments: –í—Å–µ —Å–µ–≥–º–µ–Ω—Ç—ã –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ –ø–æ–≤—Ç–æ—Ä–æ–≤

    Returns:
        True –µ—Å–ª–∏ –≥–∞–ª–ª—é—Ü–∏–Ω–∞—Ü–∏—è, False –µ—Å–ª–∏ —Ä–µ–∞–ª—å–Ω–∞—è —Ä–µ—á—å
    """
    confidence = seg.get('confidence', 0)
    duration = seg.get('end', 0) - seg.get('start', 0)
    text = seg.get('text', '').strip()

    # –ö—Ä–∏—Ç–µ—Ä–∏–π 1: –û—á–µ–Ω—å –Ω–∏–∑–∫–∏–π confidence
    if confidence < -0.5:
        # –ö—Ä–∏—Ç–µ—Ä–∏–π 2: –ö–æ—Ä–æ—Ç–∫–∞—è –¥–ª–∏—Ç–µ–ª—å–Ω–æ—Å—Ç—å
        if duration < 2.0:
            # –ö—Ä–∏—Ç–µ—Ä–∏–π 3: –ö–æ—Ä–æ—Ç–∫–∏–π —Ç–µ–∫—Å—Ç (1-2 —Å–ª–æ–≤–∞)
            word_count = len(text.split())
            if word_count <= 2:
                # –ö—Ä–∏—Ç–µ—Ä–∏–π 4: –ü—Ä–æ–≤–µ—Ä—è–µ–º –ø–æ–≤—Ç–æ—Ä—ã
                text_lower = text.lower()
                repeat_count = 0

                for other_seg in all_segments:
                    if other_seg is seg:
                        continue
                    other_text = other_seg.get('text', '').lower()
                    if text_lower in other_text or other_text in text_lower:
                        repeat_count += 1

                if repeat_count > 0:
                    return True

    return False


# ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
# –ö–û–ù–¢–ï–ö–°–¢–ù–ê–Ø –û–ß–ò–°–¢–ö–ê
# ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê

def clean_hallucinations_from_text_context(text):
    """
    –ö–æ–Ω—Ç–µ–∫—Å—Ç–Ω—ã–π —Ñ–∏–ª—å—Ç—Ä: —É–¥–∞–ª—è–µ—Ç –≥–∞–ª–ª—é—Ü–∏–Ω–∞—Ü–∏–∏ –∏–∑ —Ç–µ–∫—Å—Ç–∞, —Å–æ—Ö—Ä–∞–Ω—è—è –æ—Å—Ç–∞–ª—å–Ω–æ–µ

    Args:
        text: –¢–µ–∫—Å—Ç –¥–ª—è –æ—á–∏—Å—Ç–∫–∏

    Returns:
        –û—á–∏—â–µ–Ω–Ω—ã–π —Ç–µ–∫—Å—Ç
    """
    # üîß v16.0: –£–ë–†–ê–ù –ø–∞—Ç—Ç–µ—Ä–Ω "–î–∞–≤–∞–π—Ç–µ —Å–Ω—è—Ç—å" - —Ç–µ–ø–µ—Ä—å —ç—Ç–æ –≤–∞–ª–∏–¥–Ω–∞—è —Ñ—Ä–∞–∑–∞
    patterns_to_remove = [
        r'\b([–ü–ø]–µ—Ä–≤—ã–π|[–í–≤]—Ç–æ—Ä–æ–π|[–¢—Ç]—Ä–µ—Ç–∏–π|[–ß—á]–µ—Ç–≤—ë—Ä—Ç—ã–π|[–ß—á]–µ—Ç–≤–µ—Ä—Ç—ã–π|[–ü–ø]—è—Ç—ã–π|[–®—à]–µ—Å—Ç–æ–π|[–°—Å]–µ–¥—å–º–æ–π|[–í–≤]–æ—Å—å–º–æ–π|[–î–¥]–µ–≤—è—Ç—ã–π|[–î–¥]–µ—Å—è—Ç—ã–π|[–û–æ]–¥–∏–Ω–Ω–∞–¥—Ü–∞—Ç—ã–π|[–î–¥]–≤–µ–Ω–∞–¥—Ü–∞—Ç—ã–π|[–¢—Ç]—Ä–∏–Ω–∞–¥—Ü–∞—Ç—ã–π|[–ß—á]–µ—Ç—ã—Ä–Ω–∞–¥—Ü–∞—Ç—ã–π|[–ü–ø]—è—Ç–Ω–∞–¥—Ü–∞—Ç—ã–π|[–®—à]–µ—Å—Ç–Ω–∞–¥—Ü–∞—Ç—ã–π|[–°—Å]–µ–º–Ω–∞–¥—Ü–∞—Ç—ã–π|[–í–≤]–æ—Å–µ–º–Ω–∞–¥—Ü–∞—Ç—ã–π|[–î–¥]–µ–≤—è—Ç–Ω–∞–¥—Ü–∞—Ç—ã–π|[–î–¥]–≤–∞–¥—Ü–∞—Ç—ã–π|[–¢—Ç]—Ä–∏–¥—Ü–∞—Ç—ã–π|\d+[-–π—ã–∏]?)\s+–≤–æ–ø—Ä–æ—Å\.?\s*',
    ]

    cleaned = text
    removed_count = 0

    for pattern in patterns_to_remove:
        matches = re.findall(pattern, cleaned, flags=re.I)
        if matches:
            cleaned = re.sub(pattern, '', cleaned, flags=re.I)
            removed_count += len(matches)

    # –ù–æ—Ä–º–∞–ª–∏–∑–∞—Ü–∏—è –ø—Ä–æ–±–µ–ª–æ–≤ –∏ —Ç–æ—á–µ–∫
    cleaned = re.sub(r'\s+', ' ', cleaned)
    cleaned = re.sub(r'\.+', '.', cleaned)
    cleaned = re.sub(r'\.\s+\.', '.', cleaned)
    cleaned = re.sub(r'^\.\s*', '', cleaned)
    cleaned = re.sub(r':\s*\.', ':', cleaned)
    cleaned = cleaned.strip()

    return cleaned


# ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
# –û–°–ù–û–í–ù–ê–Ø –§–£–ù–ö–¶–ò–Ø –û–ß–ò–°–¢–ö–ò
# ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê

def clean_hallucinations_from_text(text, speaker):
    """
    –û—á–∏—â–∞–µ—Ç –≥–∞–ª–ª—é—Ü–∏–Ω–∞—Ü–∏–∏ –∏–∑ —Ç–µ–∫—Å—Ç–∞ —Å —É—á—ë—Ç–æ–º —Å–ø–∏–∫–µ—Ä–∞

    Args:
        text: –¢–µ–∫—Å—Ç –¥–ª—è –æ—á–∏—Å—Ç–∫–∏
        speaker: –ò–º—è —Å–ø–∏–∫–µ—Ä–∞ ("–ñ—É—Ä–Ω–∞–ª–∏—Å—Ç", "–û–ø–µ—Ä–∞—Ç–æ—Ä" –∏–ª–∏ —Ñ–∞–º–∏–ª–∏—è)

    Returns:
        –û—á–∏—â–µ–Ω–Ω—ã–π —Ç–µ–∫—Å—Ç
    """
    # –ñ—É—Ä–Ω–∞–ª–∏—Å—Ç –∏ –û–ø–µ—Ä–∞—Ç–æ—Ä –Ω–µ –æ—á–∏—â–∞—é—Ç—Å—è (–∏—Ö –∫–æ—Ä–æ—Ç–∫–∏–µ —Ñ—Ä–∞–∑—ã –≤–∞–ª–∏–¥–Ω—ã)
    if speaker == "–ñ—É—Ä–Ω–∞–ª–∏—Å—Ç" or speaker == "–û–ø–µ—Ä–∞—Ç–æ—Ä":
        return text

    # –ö–æ–Ω—Ç–µ–∫—Å—Ç–Ω–∞—è –æ—á–∏—Å—Ç–∫–∞ –¥–ª—è –æ—Å–Ω–æ–≤–Ω–æ–≥–æ —Å–ø–∏–∫–µ—Ä–∞
    if HALLUCINATION_CLEANUP_MODE == "context":
        return clean_hallucinations_from_text_context(text)
    else:
        return text


